def say_hello():
    print("hello everyone")


def add(a,b):
    print("add:",a,"+",b,"=",a+b)
def sub(a,b):
    print("sub:", a, "-", b, "=", a - b)
def mul(a,b):
    print("mul:",a,"*",b,"=",a*b)
def div(a,b):
    print("div:", a, "/", b, "=", a / b)
def floordiv(a,b):
    print("floordiv:", a, "//", b, "=", a // b)
def pow(a,b):
    print("power:", a, "**", b, "=", a ** b)
def mod(a,b):
    print("mod:",a,"%",b,"=",a%b)
def bye():
    print("Bye everyone!!!")

